def forwarder_shipping_usd(weight_g: float | None) -> float:
    """무게(g) → 배대지 운송비(USD).

    - weight_g 가 None / 0 / 음수 → 디폴트 3 LBS 요금 ($15.94)
    - 0.5 LBS 단위 ceil
    - 60 LBS 초과 → 60 LBS 요금
    """
    if not weight_g or weight_g <= 0:
        target_lbs = DEFAULT_LBS_NO_WEIGHT
    else:
        lbs = weight_g / GRAMS_PER_LBS
        if lbs <= 0.5:
            target_lbs = 0.5
        else:
            target_lbs = min(60, math.ceil(lbs))

    for w, p in _RATES:
        if w >= target_lbs:
            return p
    return _RATES[-1][1]
